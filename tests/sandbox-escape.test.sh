#!/bin/bash
# tests/sandbox-escape.test.sh — НЕГАТИВНЫЙ ТЕСТ ПРИЁМКИ изоляции (T6, см.
# .superpowers/sdd/iso-t6-brief.md). Формулировка задачи дословно: «намеренно написанный
# тест, который "забыл" подставить песочницу, падает с внятной ошибкой и НЕ трогает боевое».
#
# Это ТЕСТ, а не разовая проверка: забывчивая фикстура пишется здесь же, запускается здесь же
# и каждый прогон набора заново доказывает, что граница на месте. Если кто-то ослабит T1
# (резолвер), T2 (guard) или tests/lib/bootstrap.sh — красным станет ИМЕННО этот файл, с
# понятным текстом, что именно перестало ловиться.
#
# ЧТО ЗДЕСЬ ПРОВЕРЯЕТСЯ (6 сценариев, все — на ОДНОЙ и той же «забывчивой» фикстуре):
#   1. Опасность РЕАЛЬНА: та же фикстура БЕЗ пролога bootstrap, запущенная в обход раннера,
#      действительно резолвит боевой дефолт и создаёт в нём каталоги (рубеж 3 — лит — ловит
#      именно этот класс: тест без пролога). Без этого сценария остальные пять доказывали бы
#      «ничего не произошло», не показав, что могло произойти.
#   2. ГЛАВНЫЙ: фикстура С прологом, запущенная в обход раннера (маркер не выставлен) —
#      падает ДО тела, с внятной русской ошибкой + подсказкой «запусти через tests/run»,
#      и боевой каталог остаётся нетронутым.
#   3. Маркер выставлен на каталог С копиями заглушек процесс-контроля, но БЕЗ sentinel —
#      отказ ИМЕННО по sentinel (заглушки нарочно на месте: если бы их не было, рубеж T2
#      отказал бы первым и замаскировал бы то, что этот сценарий должен проверять — правка
#      после ревью T6, см. iso-t6-report.md).
#   4. Маркер направлен НА боевой каталог (тест «предусмотрительно» подсунул прод) — отказ:
#      пересечение с боевым корнем.
#   5. Маркер валиден, но заглушек процесс-контроля нет — отказ T2 ДО тела теста (иначе шов
#      резолвился бы в настоящий системный бинарь).
#   6. КОНТРОЛЬНЫЙ ПОЛОЖИТЕЛЬНЫЙ: та же самая фикстура ЧЕРЕЗ раннер — PASS, побочные эффекты
#      легли в песочницу раннера, боевой каталог по-прежнему нетронут. Смысл: граница не
#      «запрещает тестам работать», она заставляет их работать в песочнице.
#
# БЕЗОПАСНОСТЬ (почему это не опасно запускать). «Боевой каталог» здесь — НЕ настоящий
# /home/rainor/.claude-control, а $FAKE_HOME/.claude-control внутри песочницы раннера:
# каждой фикстуре HOME передаётся ЯВНО (env HOME=…), а не наследуется, поэтому профиль
# control_only ($HOME/.claude-control) резолвится внутрь песочницы даже если раннер когда-то
# перестанет подменять HOME. Хардкод-профиль auto_then_hardcoded (буквальный
# /home/rainor/.claude-control) здесь не используется НИ РАЗУ — именно потому, что он не
# зависит от HOME и увёл бы фикстуру в настоящий боевой каталог.
set -u
# shellcheck disable=SC1091
. "$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)/lib/bootstrap.sh"
# shellcheck disable=SC1091
. "$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)/lib/sandbox.sh"
# shellcheck disable=SC1091
. "$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)/lib/bootstrap-detect.sh"

DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
RUNNER="$DIR/tests/run"
fail() { echo "FAIL: $1"; exit 1; }

# --- изолированный «дом» для фикстур ------------------------------------------------------
# Внутри песочницы раннера (её раннер за собой уберёт). ВСЁ, что фикстура сочтёт «боевым
# корнем», окажется здесь.
FAKE_HOME="$CLAUDE_CONTROL_TEST_ROOT/fake-home"
mkdir -p "$FAKE_HOME" || fail "не смог создать фиктивный HOME"
FAKE_PROD="$FAKE_HOME/.claude-control"

# Страховка на случай будущей правки: фиктивный дом ОБЯЗАН лежать внутри тестового корня.
# Если это когда-нибудь перестанет быть правдой, фикстуры начнут писать в настоящий боевой
# каталог — падаем сразу, до первого запуска.
case "$FAKE_HOME" in
  "$CLAUDE_CONTROL_TEST_ROOT"/*) ;;
  *) fail "фиктивный HOME '$FAKE_HOME' вне тестового корня — фикстуры писали бы в боевое, прогон остановлен" ;;
esac

FIXTURE_DIR="$CLAUDE_CONTROL_TEST_ROOT/escape-fixtures"
mkdir -p "$FIXTURE_DIR" || fail "не смог создать каталог фикстур"

# --- фикстуры ------------------------------------------------------------------------------
# «Опасное» тело у обеих ОДИНАКОВОЕ (резолвит корень и создаёт в нём каталог воркера — ровно
# то, что делает любой тест флота). Отличие ровно одно: наличие пролога bootstrap первой
# строкой. Так сравнение честное: разницу в поведении даёт пролог, а не другое тело.
FORGOT="$FIXTURE_DIR/forgot-bootstrap.test.sh"
cat > "$FORGOT" <<EOF
#!/bin/bash
# Фикстура: тест, который забыл ВСЁ — и раннер, и пролог. Резолвит корень сам.
set -u
. "$DIR/lib/runtime-root.sh"
root="\$(resolve_runtime_root control_only)" || exit 1
mkdir -p "\$root/workers/victim-worker"
printf 'этот каталог создал тест\n' > "\$root/workers/victim-worker/MARK"
echo "ФИКСТУРА-ВЫПОЛНИЛА-ТЕЛО root=\$root"
EOF
chmod +x "$FORGOT"

WITH_BOOTSTRAP="$FIXTURE_DIR/with-bootstrap.test.sh"
cat > "$WITH_BOOTSTRAP" <<EOF
#!/bin/bash
# Фикстура: пролог на месте (как требует лит), но песочницу подставить забыли — запускают
# файл напрямую, мимо tests/run. Тело — то же самое, что у forgot-bootstrap.test.sh.
set -u
. "$DIR/tests/lib/bootstrap.sh"
root="\$(resolve_runtime_root control_only)" || exit 1
mkdir -p "\$root/workers/victim-worker"
printf 'этот каталог создал тест\n' > "\$root/workers/victim-worker/MARK"
echo "ФИКСТУРА-ВЫПОЛНИЛА-ТЕЛО root=\$root"
EOF
chmod +x "$WITH_BOOTSTRAP"

# Обе фикстуры обязаны отличаться ИМЕННО прологом — проверяем тем же детектором, которым
# рубеж 3 (tests/lint-bootstrap.test.sh) отличает мигрированный тест от немигрированного.
detect_bootstrap_connection "$FORGOT" \
  && fail "детектор считает, что forgot-bootstrap.test.sh подключает пролог — фикстура собрана неверно"
detect_bootstrap_connection "$WITH_BOOTSTRAP" \
  || fail "детектор не видит пролог в with-bootstrap.test.sh — фикстура собрана неверно (лит бы её не пропустил)"
echo "OK: фикстуры отличаются ровно прологом (проверено детектором рубежа 3)"

# =========================================================================================
# 1) Опасность реальна: БЕЗ пролога, в обход раннера — фикстура резолвит боевой дефолт и
#    создаёт в нём каталог. Это тот самый класс ошибки, ради которого T1-T6 и делались.
# =========================================================================================
[ ! -e "$FAKE_PROD" ] || fail "фиктивный боевой каталог существует ДО первого сценария — тест собран неверно"
out1="$(env -u CLAUDE_CONTROL_TEST_ROOT -u CLAUDE_CONTROL_DIR -u CLAUDE_AUTO_HOME -u DEPT_HOME \
        HOME="$FAKE_HOME" bash "$FORGOT" 2>&1)" \
  || fail "фикстура без пролога должна была отработать успешно (демонстрация опасности): $out1"
echo "$out1" | command grep -q 'ФИКСТУРА-ВЫПОЛНИЛА-ТЕЛО' \
  || fail "фикстура без пролога не выполнила тело — сценарий-демонстрация ничего не демонстрирует: $out1"
[ -f "$FAKE_PROD/workers/victim-worker/MARK" ] \
  || fail "фикстура без пролога НЕ создала каталог в боевом дефолте — значит сценарии 2-5 ниже доказывали бы пустоту"
rm -rf "$FAKE_PROD" || fail "не смог убрать следы демонстрационного сценария"
echo "OK: 1 — без пролога и без раннера тест РЕАЛЬНО пишет в боевой дефолт (опасность не гипотетическая)"

# =========================================================================================
# 2) ГЛАВНЫЙ СЦЕНАРИЙ ПРИЁМКИ: пролог на месте, песочницу «забыли» — падение с внятной
#    ошибкой ДО тела, боевое не тронуто.
# =========================================================================================
out2="$(env -u CLAUDE_CONTROL_TEST_ROOT -u CLAUDE_CONTROL_DIR -u CLAUDE_AUTO_HOME -u DEPT_HOME \
        HOME="$FAKE_HOME" bash "$WITH_BOOTSTRAP" 2>&1)" \
  && fail "тест без песочницы ВЫПОЛНИЛСЯ успешно — граница не сработала: $out2"
echo "$out2" | command grep -q 'CLAUDE_CONTROL_TEST_ROOT' \
  || fail "в отказе не назван маркер CLAUDE_CONTROL_TEST_ROOT — причина непонятна читателю: $out2"
echo "$out2" | command grep -q 'tests/run' \
  || fail "в отказе нет подсказки «запусти через tests/run» — сообщение не объясняет, что делать: $out2"
echo "$out2" | command grep -q 'ФИКСТУРА-ВЫПОЛНИЛА-ТЕЛО' \
  && fail "тело теста всё-таки выполнилось — отказ произошёл ПОСЛЕ побочных эффектов: $out2"
[ -e "$FAKE_PROD" ] \
  && fail "боевой каталог '$FAKE_PROD' создан тестом, который забыл песочницу — ГЛАВНОЕ обещание изоляции нарушено"
echo "OK: 2 — тест без песочницы падает с внятной ошибкой и НЕ трогает боевое (приёмка T6)"

# =========================================================================================
# 3) Маркер выставлен на каталог БЕЗ sentinel, но С копиями заглушек процесс-контроля —
#    отказ по sentinel (T1), НЕ по заглушкам (T2). Каталог нарочно называется без подстроки
#    "sentinel" в имени (было "no-sentinel" — сама подстрока в пути делала ассерт по 'sentinel'
#    тавтологичным: он совпадал с ЛЮБЫМ отказом, который цитирует путь, включая отказ T2 по
#    заглушкам). Заглушки кладём через make_test_subroot (tests/lib/sandbox.sh) — тот же
#    хелпер, что строит подкорни в других тестах — и убираем ТОЛЬКО sentinel, который он
#    создаёт по умолчанию: единственная оставшаяся причина возможного отказа — его отсутствие.
# =========================================================================================
make_test_subroot plain-dir PLAIN_DIR PLAIN_ENV || fail "не смог создать под-корень для сценария 3"
rm -f "$PLAIN_DIR/$_RUNTIME_ROOT_SENTINEL_NAME" \
  || fail "не смог убрать sentinel из под-корня сценария 3 (по сюжету сценария sentinel обязан отсутствовать)"
out3="$("${PLAIN_ENV[@]}" HOME="$FAKE_HOME" bash "$WITH_BOOTSTRAP" 2>&1)" \
  && fail "маркер на каталог без sentinel принят — защита от «случайно указали чужой каталог» не работает: $out3"
echo "$out3" | command grep -q 'не содержит sentinel-файл' \
  || fail "в отказе не сказано про отсутствие sentinel-файла — читателю неясно, чего не хватило: $out3"
echo "$out3" | command grep -q 'заглушка процесс-контроля' \
  && fail "отказ сценария 3 упомянул заглушку процесс-контроля — значит подкорень отказал по T2 (заглушкам), а не по sentinel; сценарий не проверяет то, что заявляет: $out3"
[ -e "$PLAIN_DIR/workers" ] && fail "тело теста успело создать каталоги в подкорне без sentinel"
[ -e "$FAKE_PROD" ] && fail "боевой каталог создан в сценарии с маркером без sentinel"
echo "OK: 3 — маркер без sentinel отвергнут ИМЕННО по sentinel (заглушки процесс-контроля были на месте), тело не выполнилось"

# =========================================================================================
# 4) Маркер направлен НА боевой каталог — тест «предусмотрительно» подложил туда sentinel,
#    чтобы пройти проверку. Резолвер обязан отказать по пересечению с боевым корнем.
# =========================================================================================
mkdir -p "$FAKE_PROD" || fail "не смог подготовить фиктивный боевой каталог"
: > "$FAKE_PROD/$_RUNTIME_ROOT_SENTINEL_NAME" || fail "не смог положить sentinel в фиктивный боевой каталог"
out4="$(env -u CLAUDE_CONTROL_DIR -u CLAUDE_AUTO_HOME -u DEPT_HOME \
        HOME="$FAKE_HOME" CLAUDE_CONTROL_TEST_ROOT="$FAKE_PROD" bash "$WITH_BOOTSTRAP" 2>&1)" \
  && fail "маркер, направленный на боевой каталог, принят — sentinel оказался достаточным паролем к боевому контуру: $out4"
echo "$out4" | command grep -q 'боевым корнем' \
  || fail "в отказе не сказано про пересечение с боевым корнем: $out4"
[ -e "$FAKE_PROD/workers" ] \
  && fail "тело теста создало workers/ в боевом каталоге — отказ произошёл слишком поздно"
rm -rf "$FAKE_PROD" || fail "не смог убрать фиктивный боевой каталог после сценария 4"
echo "OK: 4 — маркер, направленный в боевой каталог, отвергнут даже с подложенным sentinel"

# =========================================================================================
# 5) Маркер валиден (sentinel на месте), но заглушек процесс-контроля нет — рубеж 2 обязан
#    отказать ДО тела: без заглушек шов резолвится в настоящий системный бинарь.
#    Проверка T2 read-only (command -v + realpath), настоящий systemctl не запускается.
# =========================================================================================
VALID_ROOT="$CLAUDE_CONTROL_TEST_ROOT/valid-but-no-stubs"
mkdir -p "$VALID_ROOT" || fail "не смог создать корень для сценария 5"
: > "$VALID_ROOT/$_RUNTIME_ROOT_SENTINEL_NAME" || fail "не смог положить sentinel"
out5="$(env -u CLAUDE_CONTROL_DIR -u CLAUDE_AUTO_HOME -u DEPT_HOME \
        -u SYSTEMCTL -u DEPT_SYSTEMD_RUN -u TMUX_BIN \
        HOME="$FAKE_HOME" PATH=/usr/bin:/bin CLAUDE_CONTROL_TEST_ROOT="$VALID_ROOT" bash "$WITH_BOOTSTRAP" 2>&1)" \
  && fail "тест с валидным корнем, но без заглушек процесс-контроля, прошёл пролог — шов вёл бы в настоящий systemctl/tmux: $out5"
echo "$out5" | command grep -q 'заглушка процесс-контроля' \
  || fail "в отказе не сказано про заглушку процесс-контроля: $out5"
[ -e "$VALID_ROOT/workers" ] && fail "тело теста выполнилось, хотя заглушек не было"
echo "OK: 5 — валидный корень без заглушек процесс-контроля отвергнут ДО тела теста"

# =========================================================================================
# 6) КОНТРОЛЬНЫЙ ПОЛОЖИТЕЛЬНЫЙ: ТА ЖЕ фикстура, запущенная ЧЕРЕЗ раннер, проходит — раннер
#    сам строит песочницу. Граница не запрещает тесту работать, она переносит его работу в
#    песочницу. Прогон изолирован TESTS_RUN_DIR_OVERRIDE: раннер видит ТОЛЬКО наш каталог
#    фикстур, ни один настоящий тест из набора не запускается (тот же приём, что в
#    tests/run.test.sh).
# =========================================================================================
RUN_FIXTURE_DIR="$CLAUDE_CONTROL_TEST_ROOT/escape-runner-fixtures"
mkdir -p "$RUN_FIXTURE_DIR" || fail "не смог создать каталог фикстур для раннера"
cp "$WITH_BOOTSTRAP" "$RUN_FIXTURE_DIR/with-bootstrap.test.sh" || fail "не смог скопировать фикстуру"
out6="$(TESTS_RUN_DIR_OVERRIDE="$RUN_FIXTURE_DIR" HOME="$FAKE_HOME" "$RUNNER" 2>&1)" \
  || fail "та же фикстура ЧЕРЕЗ раннер обязана проходить (иначе граница ломает нормальную работу): $out6"
echo "$out6" | command grep -q 'PASS: tests/with-bootstrap.test.sh' \
  || fail "раннер не отчитался PASS по фикстуре: $out6"
echo "$out6" | command grep -q 'ФИКСТУРА-ВЫПОЛНИЛА-ТЕЛО' \
  || fail "через раннер тело фикстуры не выполнилось — песочница подставлена неверно: $out6"
[ -e "$FAKE_PROD" ] \
  && fail "прогон через раннер создал боевой каталог '$FAKE_PROD' — песочница протекла"
echo "OK: 6 — та же фикстура через раннер проходит, побочные эффекты остались в песочнице"

echo "PASS sandbox-escape"
