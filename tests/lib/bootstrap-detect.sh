#!/bin/bash
# tests/lib/bootstrap-detect.sh — общий детектор "тестовый файл реально подключает
# tests/lib/bootstrap.{sh,mjs} как ПЕРВОЕ значимое действие" (T3 ревью, находка К1, см.
# .superpowers/sdd/iso-t3-report.md). Используется И tests/run (uses_bootstrap — решает,
# форсить ли SYSTEMCTL/DEPT_SYSTEMD_RUN/TMUX_BIN), И tests/lint-bootstrap.test.sh
# (has_bootstrap — рубеж 3).
#
# Вынесено в ОБЩИЙ файл (было: намеренно зеркалящийся "один трибиальный grep" в двух местах,
# обоснование — "substring, не логика T1/T2, дублировать нормально"). После фикса К1 детектор
# — state-machine на десятки строк (first-significant-line + allow-list пред-строк +
# comment-strip), а не одна строка. Дублировать ТАКОЕ в двух файлах — гарантированный
# источник расхождения (ревьюер прямо просит "не должны разъехаться" — см. iso-t3-brief.md).
# Общий файл делает расхождение структурно невозможным: оба потребителя вызывают ОДНУ и ту же
# функцию, разойтись просто негде.
#
# ПОЧЕМУ "первая значимая строка", а не "упоминание подключения где-то в файле": голый
# substring-по-всему-файлу (T3 раунд 1, якорный regex `^[[:space:]]*(\.[[:space:]]|import\>)
# .*lib/bootstrap\.(sh|mjs)`, применённый К КАЖДОЙ строке файла БЕЗ учёта позиции) засчитывал
# файл защищённым в 4 случаях, где реальной защиты нет (см. iso-t3-report.md К1, все 4
# воспроизведены тем же якорным regex, что был в коде):
#   1. `. /dev/null # lib/bootstrap.sh` — реальный no-op source (аргумент — /dev/null),
#      путь к bootstrap упомянут ТОЛЬКО в комментарии на ТОЙ ЖЕ строке — старый regex не
#      резал строку по '#' перед проверкой substring'а.
#   2. bootstrap-путь спрятан внутри heredoc-заглушки (`: <<'X' ... lib/bootstrap.sh ... X`)
#      — данные для no-op `:`, никогда не исполняется как source; старый regex сканировал
#      ВСЕ строки файла без разбора "это код или данные heredoc'а".
#   3. `if false; then . tests/lib/bootstrap.sh; fi` — недостижимая ветка; старый regex не
#      знал о позиции строки внутри управляющей конструкции.
#   4. bootstrap подключён ПОСЛЕДНЕЙ строкой файла, ПОСЛЕ произвольного опасного кода — весь
#      код выше уже выполнился бы до какой-либо защиты при обходе раннера; старый regex не
#      проверял, что совпадение — ПЕРВОЕ значимое действие файла, только что оно СУЩЕСТВУЕТ.
# Общее у всех 4: bootstrap ЕСТЬ где-то в файле, но не ПЕРВЫМ исполняемым действием. Значит
# единственная механическая гарантия (без полного парсинга shell/JS — бриф прямо запрещает
# "полноценный парсинг shell — overkill") — потребовать, чтобы совпадение было НА ПЕРВОЙ
# строке файла, которая хоть что-то делает: не шебанг, не пустая, не комментарий.
#
# Единственное разрешённое исключение из "первая строка — обязана быть bootstrap": `set ...`
# для *.test.sh (`set -u`/`set -euo pipefail`/просто `set`) — переключает опции шелла, НЕ
# исполняет произвольный код, и КАЖДЫЙ существующий T3-файл (tests/bootstrap.test.sh,
# tests/run.test.sh, tests/lint-bootstrap.test.sh) делает `set -u` ПЕРЕД bootstrap — запрет
# этого паттерна without-exception сломал бы существующую конвенцию репозитория без всякой
# причины (см. .superpowers/sdd/iso-t3-brief.md «Конвенции репозитория»: "set -u в
# исполняемых скриптах"). У *.test.mjs аналога `set` нет — там перед import допустимы только
# пустые строки и `//`-комментарии.
#
# Heredoc-трекинг (упомянутый в ревью как "дополнительная" защита) СОЗНАТЕЛЬНО НЕ реализован
# отдельным состоянием: при алгоритме "первая значимая строка ОБЯЗАНА совпасть, иначе FAIL
# немедленно" открывающая heredoc-строка (`: <<'X'`, `cat <<EOF` и т.п.) САМА становится
# "первой значимой строкой" и не совпадает с якорем bootstrap — файл уже корректно
# признаётся НЕзащищённым, без необходимости заглядывать внутрь heredoc-тела вовсе (случай 2
# проверен явно в tests/bootstrap-detect.test.sh). Отдельный heredoc state-machine добавил бы
# код без закрытия дополнительной дыры — не делаем (тот же принцип "не overkill", что и
# запрет полного парсинга shell).
#
# НЕ `set -u` здесь (source-able библиотека, тот же довод, что у lib/*.sh/tests/lib/bootstrap.sh
# — `set -u` в коде верхнего уровня протекает в шелл ВЫЗЫВАЮЩЕГО через `.` source).

# detect_bootstrap_connection <abs-file>: 0, если ПЕРВАЯ значимая строка файла реально
# подключает bootstrap (`. .../lib/bootstrap.sh` для *.test.sh, определяется по расширению
# файла; `import '.../lib/bootstrap.mjs';` для *.test.mjs) — иначе 1. "Значимая" = не
# шебанг (только строка 1), не пустая, не комментарий, и (только для .sh) не `set ...`.
detect_bootstrap_connection() {
  local file="$1" ext lineno=0 line code

  if [[ "$file" == *.mjs ]]; then
    ext=mjs
  else
    # shellcheck disable=SC2209  # ЛОЖНОЕ срабатывание: "sh" здесь ЛИТЕРАЛЬНАЯ строка-метка
    # расширения, не имя команды — shellcheck путает `ext=sh` с "забыли var=$(sh)" (директива
    # не встанет ПЕРЕД веткой case — SC1124, поэтому if/else вместо case для этого выбора).
    ext=sh
  fi

  while IFS= read -r line || [ -n "$line" ]; do
    lineno=$((lineno + 1))

    # Шебанг — только самая первая строка файла.
    if [ "$lineno" -eq 1 ] && [[ "$line" == '#!'* ]]; then
      continue
    fi
    # Пустая строка (только пробелы/табы или ничего).
    [[ "$line" =~ ^[[:space:]]*$ ]] && continue

    if [ "$ext" = "sh" ]; then
      # Строка-комментарий целиком.
      [[ "$line" =~ ^[[:space:]]*# ]] && continue
      # Допустимая пред-bootstrap директива (см. заголовок файла — конвенция репозитория).
      [[ "$line" =~ ^[[:space:]]*set([[:space:]]|$) ]] && continue

      # Первая ЗНАЧИМАЯ строка — обязана быть реальным source bootstrap.sh. Отрезаем всё
      # ПОСЛЕ первого '#' (trailing-комментарий, находка К1.1: `. /dev/null # lib/bootstrap.sh`
      # засчитывался бы substring'ом по всей строке, хотя реальный аргумент source — /dev/null,
      # а "lib/bootstrap.sh" — только текст комментария).
      code="${line%%#*}"
      if [[ "$code" =~ ^[[:space:]]*\.[[:space:]] ]] && [[ "$code" == *"lib/bootstrap.sh"* ]]; then
        return 0
      fi
      return 1
    else
      # .mjs: строка-комментарий целиком ('//').
      [[ "$line" =~ ^[[:space:]]*// ]] && continue

      # Отрезаем всё после первого '//' (тот же trailing-comment довод, что у .sh выше).
      code="${line%%//*}"
      if { [[ "$code" =~ ^[[:space:]]*import[[:space:]]+\" ]] \
           || [[ "$code" =~ ^[[:space:]]*import[[:space:]]+\' ]]; } \
         && [[ "$code" == *"lib/bootstrap.mjs"* ]]; then
        return 0
      fi
      return 1
    fi
  done < "$file"

  # Дошли до конца файла, не встретив НИ ОДНОЙ значимой строки (файл пуст или целиком из
  # шебанга/пустых/комментариев/set) — bootstrap точно не подключён.
  return 1
}
