#!/bin/bash
# tests/notify-card.test.sh — рендер и отправка операторских карточек (lib/notify-card.sh
# и её JS-зеркало lib/notify-card.js).
#
# Главное, что здесь проверяется, — НЕ красота вёрстки, а то, что переход на parse_mode=HTML
# не начал ТИХО ТЕРЯТЬ алерты. У telegram_notify.sh фолбэк только транспортный (VPN), на
# ошибку разметки он просто отдаёт exit 1 — а булев результат доставки у liveness/dispatcher
# несущий (эпизод помечается «уведомлён» только по факту доставки). Отсюда сценарии 6-9:
# ретрай обязан быть, обязан срабатывать ТОЛЬКО на разметке и ровно один раз.
set -u
# shellcheck disable=SC1091
. "$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)/lib/bootstrap.sh"
DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
ROOT="$CLAUDE_CONTROL_TEST_ROOT"
LIB="$DIR/lib/notify-card.sh"

fail() { echo "FAIL: $1"; exit 1; }

# Свой .env внутри песочницы: боевой /home/rainor/server/.env тест не трогает и не читает.
GOOD_ENV="$ROOT/nc-good.env"; printf 'RNR_WORKERS_BOT_TOKEN=123456:AAbb-cc_dd\n' > "$GOOD_ENV"
BAD_ENV="$ROOT/nc-bad.env";  printf 'RNR_WORKERS_BOT_TOKEN=НЕ_ТОКЕН\n' > "$BAD_ENV"

# ---------------------------------------------------------------------------------------
# 1. Экранирование во ВСЕХ позициях карточки. Неэкранированный '<' из stderr-хвоста или
#    e.message — самый вероятный способ потерять алерт целиком (Telegram отвергает всё
#    сообщение), поэтому проверяем каждое поле, а не одно «показательное».
# ---------------------------------------------------------------------------------------
# Злая строка передаётся АРГУМЕНТОМ, а не интерполяцией в текст скрипта: вложенные bash -c
# съедали бы кавычки и '&' сами, и тест ловил бы собственную кривость вместо поведения
# библиотеки (ровно на этом он и упал в первой редакции).
evil='<b>&"x"</b>'
out="$(bash -c '
  . "$0"
  e="$1"
  nc_reset
  nc_head "📢" "T$e" "S$e"
  nc_field "K$e" "V$e"
  nc_section "✅" "SEC$e" "item$e"
  nc_block "🔴" "BLK$e" "body$e"
  nc_quote "Q$e"
  nc_action "A$e"
  nc_render' "$LIB" "$evil")"
# Ни один литеральный тег из данных не должен уцелеть — уцелеть должны только теги разметки,
# которые ставит сама библиотека (<b>/<i>/<code> вокруг ЗАГОЛОВКОВ, не вокруг значений).
for f in T S K V SEC BLK Q A; do
  printf '%s' "$out" | grep -qF "${f}&lt;b&gt;&amp;\"x\"&lt;/b&gt;" \
    || fail "1: поле $f не экранировано: $out"
done
[ "$(printf '%s' "$out" | grep -c '<b>')" = "$(printf '%s' "$out" | grep -c '</b>')" ] \
  || fail "1: несбалансированные <b> после экранирования: $out"
echo "OK: 1 — экранирование во всех позициях карточки"

# ---------------------------------------------------------------------------------------
# 2. Пустая секция не печатается ВООБЩЕ. Ради этого всё и затевалось: сегодня «🔴 не
#    разослано — нет» занимает в ленте столько же места, сколько реальный отказ.
# ---------------------------------------------------------------------------------------
out="$(bash -c '
  . "$0"
  nc_reset; nc_head "📢" "H"
  nc_section "🔴" "Не доставлено" ""
  nc_section "💤" "Спят" "   "
  nc_field "Пусто" ""
  nc_render' "$LIB")"
printf '%s' "$out" | grep -q 'Не доставлено' && fail "2: пустая секция отрендерилась: $out"
printf '%s' "$out" | grep -q 'Спят'          && fail "2: секция из пробелов отрендерилась: $out"
printf '%s' "$out" | grep -q 'Пусто'         && fail "2: поле с пустым значением отрендерилось: $out"
echo "OK: 2 — пустые секции и поля не печатаются"

# ---------------------------------------------------------------------------------------
# 3. Бюджет. Меряем в UTF-16 code units (единицы лимита Telegram): для не-BMP эмодзи они
#    вдвое расходятся с кодовыми точками, и наивный ${#s} пропустил бы сообщение, которое
#    Telegram отвергнет. Заголовок и «Что делать» обязаны выжить при любом переполнении.
# ---------------------------------------------------------------------------------------
big="$(for i in $(seq 1 400); do printf 'воркер-с-длинным-именем-%s ' "$i"; done)"
out="$(bash -c '
  . "$0"
  nc_reset; nc_head "📢" "Заголовок выживает"
  nc_section "✅" "Доставлено" "$1"
  nc_quote "цитата которая должна быть вытеснена раньше секций"
  nc_action "ЭТО ДЕЙСТВИЕ ОБЯЗАНО ВЫЖИТЬ"
  nc_render' "$LIB" "$big")"
u16="$(bash -c '. "$0"; nc_u16len "$1"' "$LIB" "$out")"
[ "$u16" -le 3500 ] || fail "3: бюджет превышен ($u16 > 3500)"
printf '%s' "$out" | grep -q 'Заголовок выживает'        || fail "3: заголовок вытеснен: $out"
printf '%s' "$out" | grep -q 'ЭТО ДЕЙСТВИЕ ОБЯЗАНО ВЫЖИТЬ' || fail "3: «Что делать» вытеснено"
printf '%s' "$out" | grep -q 'ещё '                      || fail "3: список не свёрнут в «… (ещё N)»"

# Не-BMP эмодзи: 2000 штук = 2000 кодовых точек, но 4000 UTF-16 units. Наивная мерка по
# кодовым точкам сочла бы это влезающим — и Telegram отверг бы сообщение целиком.
emo="$(bash -c '. "$0"; nc_u16len "$(printf "🙂%.0s" $(seq 1 2000))"' "$LIB")"
[ "$emo" = 4000 ] || fail "3: nc_u16len меряет не в UTF-16 (2000×🙂 дало $emo, ожидалось 4000)"
echo "OK: 3 — бюджет в UTF-16, заголовок и «Что делать» выживают"

# ---------------------------------------------------------------------------------------
# 4. Рендер никогда не рвёт тег или сущность. Обрезка готового HTML посередине <b> или
#    &amp; даёт ровно тот 400, от которого защищаемся, — поэтому бюджет применяется
#    ПЕРЕСБОРКОЙ, а не ножом по строке.
# ---------------------------------------------------------------------------------------
for n in 50 200 800 3000; do
  o="$(bash -c '
    . "$0"
    nc_reset; nc_head "📢" "H"
    nc_section "✅" "S" "$(for i in $(seq 1 "$1"); do printf "элемент-%s " "$i"; done)"
    nc_render' "$LIB" "$n")"
  [ "$(printf '%s' "$o" | grep -c '<b>')" = "$(printf '%s' "$o" | grep -c '</b>')" ] \
    || fail "4: рваный тег при n=$n"
  printf '%s' "$o" | grep -qE '&(amp|lt|gt)?$' && fail "4: рваная HTML-сущность при n=$n"
done
echo "OK: 4 — теги и сущности целые на всех размерах"

# ---------------------------------------------------------------------------------------
# 5. Паритет bash- и JS-рендеров. Оба сторожа отдела написаны на разных языках, и оператор
#    не должен получать два разных визуальных языка в зависимости от того, кто его позвал.
# ---------------------------------------------------------------------------------------
sh_out="$(bash -c '
  . "$0"
  nc_reset
  nc_head "📢" "Планёрка отдела" "policy-v14"
  nc_field "Итог" "8 из 11"
  nc_section "✅" "Доставлено" "w1 w2 w3 w4 w5 w6 w7 w8"
  nc_section "🔴" "Не доставлено" "dept-tp"
  nc_section "🟡" "Пустая" ""
  nc_block "🔴" "Ошибка" "stderr <хвост> & прочее"
  nc_quote "причина <со скобками>"
  nc_action "действие"
  nc_render' "$LIB")"
js_out="$(node -e '
const c = require(process.argv[1]);
process.stdout.write(c.build({
  head: ["📢","Планёрка отдела","policy-v14"],
  fields: [["Итог","8 из 11"]],
  sections: [
    {emoji:"✅", title:"Доставлено", items:"w1 w2 w3 w4 w5 w6 w7 w8"},
    {emoji:"🔴", title:"Не доставлено", items:"dept-tp"},
    {emoji:"🟡", title:"Пустая", items:""},
    {emoji:"🔴", title:"Ошибка", text:"stderr <хвост> & прочее"},
  ],
  quote: "причина <со скобками>",
  action: "действие",
}));' "$DIR/lib/notify-card.js")"
[ "$sh_out" = "$js_out" ] || fail "5: рендеры разошлись
--- sh ---
$sh_out
--- js ---
$js_out"
echo "OK: 5 — bash- и JS-рендеры совпадают побайтово"

# ---------------------------------------------------------------------------------------
# Заглушки нотификатора для сценариев отправки. NC_MODE управляет поведением:
#   ok      — успех
#   markup  — ошибка РАЗМЕТКИ (400) на вызове с --parse-mode, успех без него
#   transport — транспортный сбой всегда (ретрая быть НЕ должно)
# ---------------------------------------------------------------------------------------
STUB="$ROOT/nc-stub.sh"; CALLS="$ROOT/nc-calls.log"
cat > "$STUB" <<'STUBEOF'
#!/bin/bash
# Переводы строк внутри аргумента заменяем на литерал \n: карточка многострочная, и без
# этого ОДИН вызов растёкся бы на несколько строк лога — счётчик вызовов начал бы врать.
{ printf '%s' "CALL"; for a in "$@"; do printf '\t%s' "${a//$'\n'/\\n}"; done; printf '\n'; } >> "$NC_CALLS"
has_pm=0; for a in "$@"; do [ "$a" = "--parse-mode" ] && has_pm=1; done
case "${NC_MODE:-ok}" in
  markup)    [ "$has_pm" = 1 ] && { echo '{"ok":false,"error_code":400,"description":"Bad Request: can'"'"'t parse entities"}' >&2; exit 1; }
             echo "✅ Отправлено (direct), message_id=1"; exit 0 ;;
  transport) echo "❌ Telegram: отправка не удалась (оба канала). Ответ: <пусто>" >&2; exit 1 ;;
  *)         echo "✅ Отправлено (direct), message_id=1"; exit 0 ;;
esac
STUBEOF
chmod +x "$STUB"

nc_run() {  # nc_run <mode> <env-файл> [доп-аргументы nc_send]
  local mode="$1" envf="$2"; shift 2
  : > "$CALLS"
  NC_MODE="$mode" NC_CALLS="$CALLS" NC_ENV_FILE="$envf" NC_TG="$STUB" \
    bash -c '
      lib="$1"; shift
      . "$lib"
      nc_reset; nc_head "📢" "H"; nc_field "K" "V"
      nc_send "$@" && echo "SEND_OK" || echo "SEND_FAIL"' _ "$LIB" "$@"
}

# ---------------------------------------------------------------------------------------
# 6. Успешная отправка: ровно один вызов, с --parse-mode HTML и с `--` перед текстом.
#    `--` важен: сегодня его нет в 7 точках, и сообщение с ведущим '-' было бы перечитано
#    нотификатором как флаг.
# ---------------------------------------------------------------------------------------
[ "$(nc_run ok "$GOOD_ENV")" = "SEND_OK" ] || fail "6: успешная отправка вернула неуспех"
[ "$(grep -c "^CALL" "$CALLS")" = 1 ] || fail "6: вызовов не 1: $(cat "$CALLS")"
grep -q $'\t--parse-mode\tHTML' "$CALLS" || fail "6: нет --parse-mode HTML: $(cat "$CALLS")"
grep -q $'\t--\t' "$CALLS"               || fail "6: нет `--` перед текстом: $(cat "$CALLS")"
grep -q $'\t--bot-token-env\tRNR_WORKERS_BOT_TOKEN' "$CALLS" || fail "6: не выбран @RnR_Workers"
echo "OK: 6 — успешная отправка: один вызов, HTML, --, RnR-бот"

# ---------------------------------------------------------------------------------------
# 7. Ошибка РАЗМЕТКИ → ровно один ретрай, БЕЗ --parse-mode, и текст без «&lt;» (иначе
#    оператор увидел бы сущности вместо символов). Итог — успех: постоянный HTML-фейл иначе
#    либо заспамил бы оператора каждым тиком, либо навсегда потерял бы эпизод.
# ---------------------------------------------------------------------------------------
[ "$(nc_run markup "$GOOD_ENV")" = "SEND_OK" ] || fail "7: ретрай не спас отправку"
[ "$(grep -c "^CALL" "$CALLS")" = 2 ] || fail "7: ожидалось 2 вызова, получено $(grep -c "^CALL" "$CALLS"): $(cat "$CALLS")"
tail -1 "$CALLS" | grep -q -- '--parse-mode' && fail "7: ретрай ушёл снова с --parse-mode"
tail -1 "$CALLS" | grep -q '&lt;\|&amp;\|<b>' && fail "7: в plain-ретрае остались теги/сущности: $(tail -1 "$CALLS")"
echo "OK: 7 — ошибка разметки: ровно один plain-ретрай, без тегов и сущностей"

# ---------------------------------------------------------------------------------------
# 8. ТРАНСПОРТНЫЙ сбой ретрая НЕ вызывает. У telegram_notify.sh на это свой фолбэк
#    (direct→VPN→ssl-retry ≈ 42с); слепой второй прогон удвоил бы это и вылез за таймаут
#    вызывающего — то есть «починка» съела бы алерт вернее самой поломки.
# ---------------------------------------------------------------------------------------
[ "$(nc_run transport "$GOOD_ENV")" = "SEND_FAIL" ] || fail "8: транспортный сбой отдал успех"
[ "$(grep -c "^CALL" "$CALLS")" = 1 ] || fail "8: на транспортном сбое был ретрай: $(cat "$CALLS")"
echo "OK: 8 — транспортный сбой не ретраится и честно возвращает неуспех"

# ---------------------------------------------------------------------------------------
# 9. Сломанный токен → уходим ДЕФОЛТНЫМ ботом (argv без --bot-token-env), а не молчим.
#    Доставка важнее косметики: вернётся шапка «🖥️ Сервер», но алерт доедет.
# ---------------------------------------------------------------------------------------
[ "$(nc_run ok "$BAD_ENV")" = "SEND_OK" ] || fail "9: сломанный токен привёл к отказу от отправки"
grep -q -- '--bot-token-env' "$CALLS" && fail "9: невалидный токен всё равно выбран: $(cat "$CALLS")"
[ "$(nc_run ok "$ROOT/нет-такого-файла.env")" = "SEND_OK" ] || fail "9: нечитаемый .env привёл к отказу"
grep -q -- '--bot-token-env' "$CALLS" && fail "9: при нечитаемом .env выбран RnR-бот"
echo "OK: 9 — сломанный/отсутствующий токен: фолбэк на дефолтного бота, не молчание"

# ---------------------------------------------------------------------------------------
# 10. chat-id: минус ЗНАЧИМ (у групп он отрицательный). Нынешние вызывающие тянут его через
#     awk gsub(/[^0-9]/,"") и молча съедают знак — сюда эту ошибку не переносим.
# ---------------------------------------------------------------------------------------
[ "$(nc_run ok "$GOOD_ENV" --chat-id -1001234567890)" = "SEND_OK" ] || fail "10: отрицательный chat-id отвергнут"
grep -q $'\t--chat-id\t-1001234567890' "$CALLS" || fail "10: знак chat-id потерян: $(cat "$CALLS")"
[ "$(nc_run ok "$GOOD_ENV" --chat-id 'не-число')" = "SEND_FAIL" ] || fail "10: нечисловой chat-id принят"
[ "$(grep -c "^CALL" "$CALLS")" = 0 ] || fail "10: при нечисловом chat-id всё равно был вызов нотификатора"
echo "OK: 10 — chat-id: знак сохранён, мусор отвергнут до вызова"

# ---------------------------------------------------------------------------------------
# 11. JS-отправка: тот же контракт (ok/out), тот же ретрай только на разметке.
# ---------------------------------------------------------------------------------------
: > "$CALLS"
js_res="$(NC_MODE=markup NC_CALLS="$CALLS" NC_ENV_FILE="$GOOD_ENV" node -e '
const c = require(process.argv[1]);
const r = c.send(c.build({head:["📢","H"],fields:[["K","V"]]}), {tg: process.argv[2]});
process.stdout.write(r.ok ? "OK" : "FAIL");' "$DIR/lib/notify-card.js" "$STUB")"
[ "$js_res" = "OK" ] || fail "11: JS-отправка не пережила ошибку разметки"
[ "$(grep -c "^CALL" "$CALLS")" = 2 ] || fail "11: JS сделал не 2 вызова: $(cat "$CALLS")"
tail -1 "$CALLS" | grep -q -- '--parse-mode' && fail "11: JS-ретрай снова с --parse-mode"

: > "$CALLS"
js_res="$(NC_MODE=transport NC_CALLS="$CALLS" NC_ENV_FILE="$GOOD_ENV" node -e '
const c = require(process.argv[1]);
const r = c.send(c.build({head:["📢","H"]}), {tg: process.argv[2]});
process.stdout.write(r.ok ? "OK" : "FAIL");' "$DIR/lib/notify-card.js" "$STUB")"
[ "$js_res" = "FAIL" ] || fail "11: JS отдал успех на транспортном сбое"
[ "$(grep -c "^CALL" "$CALLS")" = 1 ] || fail "11: JS ретраил транспортный сбой"
echo "OK: 11 — JS-отправка: тот же контракт и та же политика ретрая"

# ---------------------------------------------------------------------------------------
# 12. Бюджет держится и на ОДИНОЧНЫХ длинных значениях, а не только на списках (находка
#     ревью). Лестница вытеснения раньше ужимала лишь списки, поэтому карточка с одним
#     огромным nc_block (хвост stderr) проваливалась в аварийную обрезку готового HTML —
#     разрез внутри тега давал ровно тот 400, от которого вся эта машинерия защищает.
# ---------------------------------------------------------------------------------------
for kind in block quote; do
  o="$(bash -c '
    . "$0"
    nc_reset; nc_head "🔴" "Заголовок выживает"; nc_action "ДЕЙСТВИЕ ВЫЖИВАЕТ"
    if [ "$1" = block ]; then nc_block "🔴" "Ошибка" "$(printf "x%.0s" $(seq 1 9000))"
    else nc_quote "$(printf "🙂%.0s" $(seq 1 5000))"; fi
    nc_render' "$LIB" "$kind")"
  u="$(bash -c '. "$0"; nc_u16len "$1"' "$LIB" "$o")"
  [ "$u" -le 3500 ] || fail "12/$kind: бюджет превышен ($u)"
  printf '%s' "$o" | grep -q 'Заголовок выживает'   || fail "12/$kind: заголовок вытеснен"
  printf '%s' "$o" | grep -q 'ДЕЙСТВИЕ ВЫЖИВАЕТ'    || fail "12/$kind: «Что делать» вытеснено"
  [ "$(printf '%s' "$o" | grep -c '<b>')" = "$(printf '%s' "$o" | grep -c '</b>')" ] \
    || fail "12/$kind: рваный тег после укладки в бюджет"
done
# Обрезка меряет в ТЕХ ЖЕ единицах, что и лимит. Раньше резала по кодовым точкам: 3600
# эмодзи «обрезанных до 3500» давали 6991 UTF-16 unit — вдвое выше потолка Telegram (4096).
tr16="$(bash -c '. "$0"; nc_u16len "$(nc_trunc_u16 "$(printf "🙂%.0s" $(seq 1 3600))" 3500)"' "$LIB")"
[ "$tr16" -le 3500 ] || fail "12: nc_trunc_u16 режет не в UTF-16 (дало $tr16 при лимите 3500)"
echo "OK: 12 — бюджет держится на одиночных длинных значениях, обрезка в UTF-16"

# ---------------------------------------------------------------------------------------
# 13. Паритет рендеров сохраняется и на НЕ-BMP входе (находка ревью): bash меряет ширину
#     переноса кодовыми точками, JS раньше — UTF-16, и списки эмодзи разъезжались.
# ---------------------------------------------------------------------------------------
emolist="$(printf '🙂 %.0s' $(seq 1 14))"
a="$(bash -c '. "$0"; _nc_wrap "$1" 0' "$LIB" "$emolist")"
b="$(node -e 'const c = require(process.argv[1]); process.stdout.write(c.wrapItems(process.argv[2], 0));' "$DIR/lib/notify-card.js" "$emolist")"
[ "$a" = "$b" ] || fail "13: рендеры расходятся на эмодзи-списке
--sh--
$a
--js--
$b"
echo "OK: 13 — паритет держится и на не-BMP списках"

# ---------------------------------------------------------------------------------------
# 14. Разбиение списка НЕ делает pathname expansion (находка ревью). Токен с `*` иначе
#     подставил бы в карточку имена файлов из текущего каталога.
# ---------------------------------------------------------------------------------------
globdir="$ROOT/globtest"; mkdir -p "$globdir"; : > "$globdir/воркер-секретный-файл"
got="$(cd "$globdir" && bash -c '. "$0"; _nc_wrap "воркер-* legion2" 0' "$LIB")"
printf '%s' "$got" | grep -q 'секретный-файл' && fail "14: глоб раскрылся, имя файла утекло в карточку: $got"
printf '%s' "$got" | grep -q 'воркер-\*'       || fail "14: токен с глобом потерян: $got"
echo "OK: 14 — списки не подвержены pathname expansion"

# ---------------------------------------------------------------------------------------
# 15. Отозванный (но синтаксически валидный) токен @RnR_Workers → последняя попытка
#     ДЕФОЛТНЫМ ботом (находка ревью). nc_botflag такой токен пропускает, Telegram отвечает
#     401/403, и на best-effort точках (их большинство, `|| true`) алерт пропал бы молча,
#     хотя дефолтный бот жив.
# ---------------------------------------------------------------------------------------
cat > "$ROOT/nc-stub-rnr.sh" <<'STUBEOF'
#!/bin/bash
{ printf '%s' "CALL"; for a in "$@"; do printf '\t%s' "${a//$'\n'/\\n}"; done; printf '\n'; } >> "$NC_CALLS"
# Отказ ТОЛЬКО когда просят слать ботом RnR (эмуляция отозванного токена); дефолтный — ок.
for a in "$@"; do [ "$a" = "RNR_WORKERS_BOT_TOKEN" ] && { echo '{"ok":false,"error_code":401,"description":"Unauthorized"}' >&2; exit 1; }; done
echo "✅ Отправлено (direct), message_id=7"; exit 0
