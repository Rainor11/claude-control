# nc_send [--chat-id <id>] [--tg <путь>]
# Путь нотификатора берётся из $NC_TG (или --tg) — библиотека его НЕ резолвит (см. шапку).
# Возврат: 0 — доставлено, 1 — нет. Вывод нотификатора — в $NC_LAST_OUT.
nc_send() {
  local chat="" tg="${NC_TG:-}"
  while [ $# -gt 0 ]; do
    case "$1" in
      --chat-id) chat="${2:-}"; shift 2 ;;
      --tg)      tg="${2:-}"; shift 2 ;;
      *) echo "notify-card: неизвестный аргумент nc_send: $1" >&2; return 2 ;;
    esac
  done
  NC_LAST_OUT=""
  [ -n "$tg" ] || { echo "notify-card: NC_TG не задан — путь нотификатора обязан задать вызывающий" >&2; return 1; }
  [ -x "$tg" ] || return 1
  local html; html="$(nc_render)"
  [ -n "$html" ] || return 1

  local -a args=()
  local bf; bf="$(nc_botflag)"
  [ -n "$bf" ] && args+=(--bot-token-env "$bf")
  if [ -n "$chat" ]; then
    # ^-?[0-9]+$ — минус ЗНАЧИМ: у групп chat_id отрицательный. Нынешние вызывающие тянут
    # его через `gsub(/[^0-9]/,"",$2)` (claude-auto-run:130, event-bridge-watch:110), что
    # молча съедает знак; сюда эту ошибку не тащим.
    [[ "$chat" =~ ^-?[0-9]+$ ]] || { echo "notify-card: chat-id '$chat' не число" >&2; return 1; }
    args+=(--chat-id "$chat")
  fi

  # `--` завершает разбор флагов нотификатором → карточка никогда не будет перечитана как флаг.
  local out rc
  out="$("$tg" "${args[@]}" --parse-mode HTML -- "$html" 2>&1)"; rc=$?
  if [ "$rc" != 0 ] && nc_is_markup_error "$out"; then
    out="$("$tg" "${args[@]}" -- "$(nc_plain "$html")" 2>&1)"; rc=$?
  fi
  # Отозванный/разавторизованный токен @RnR_Workers синтаксически валиден, поэтому nc_botflag
  # его пропускает, а Telegram отвечает 401/403 — и на best-effort точках (их большинство:
  # `|| true`) алерт молча пропадал бы, хотя дефолтный бот жив. Последняя попытка — ИМ, без
  # флага бота. Ретрай только если флаг реально был: иначе это был бы повтор того же вызова.
  if [ "$rc" != 0 ] && [ -n "$bf" ] && nc_is_auth_error "$out"; then
    local -a plainargs=()
    [ -n "$chat" ] && plainargs+=(--chat-id "$chat")
    out="$("$tg" "${plainargs[@]}" -- "$(nc_plain "$html")" 2>&1)"; rc=$?
  fi
  NC_LAST_OUT="$out"
  return "$rc"
}
