#!/bin/bash
# lib/notify-card.sh — единый рендер операторских уведомлений claude-control («карточка»).
#
# ЗАЧЕМ. До этого файла каждая точка отправки писала свою строку руками, и оператор получал
# алерты флота одной сплошной фразой с точками с запятой («Планёрка отдела («…»): разослано
# v14 — w1 w2 w3; спят — нет; 🔴 не разослано — нет. Сессии не пересобирались…»). Структуры
# нет, пустые сегменты («не разослано — нет») занимают столько же места, сколько реальная
# проблема. При этом корп-формат в репозитории УЖЕ был — bin/claude-auto-ask и
# bin/claude-auto-self-probes шлют HTML-карточки, и bot/rnr_workers_bot.py свёрстан так же.
# Этот файл выносит тот же стиль в одно место, чтобы он применялся везде, а не в трёх точках.
#
# ГРАНИЦА ОТВЕТСТВЕННОСТИ (жёсткая, решение ревью). Библиотека владеет ТОЛЬКО рендером,
# экранированием, отбрасыванием пустых секций и бюджетом длины. Путь нотификатора, chat-id,
# --reply-markup, разбор message_id, стейт доставки и своя политика fallback остаются у
# вызывающего — nc_send принимает УЖЕ отрезолвленный путь через $NC_TG и сама его не ищет.
# Причина не косметическая: bin/claude-auto-{tg,ask,send,run} держат путь нотификатора
# абсолютным и НЕперекрываемым (шов T8), и любой резолв каталога до присваивания этого пути
# — ровно та дыра, которую T8 закрыл (tests/notifier-seam.test.sh сценарий 9: подменённый
# через окружение `readlink` уводил source библиотеки в чужой каталог). Поэтому эти четыре
# файла эту библиотеку в бою НЕ подключают вовсе.
#
# НЕ `set -u` здесь намеренно — та же причина, что в lib/runtime-root.sh: это source-able
# библиотека, и `set -u` в коде верхнего уровня протекает в шелл вызывающего через `.`.
# Внутри файла каждое обращение к переменной защищено явным ${VAR:-}.
#
# Usage:
#   BINDIR="$(cd "$(dirname "$0")" && pwd)"
#   . "$BINDIR/../lib/notify-card.sh"
#   nc_reset
#   nc_head "📢" "Планёрка отдела"
#   nc_field "Правила" "policy-v14"
#   nc_section "✅" "Доставлено" "$sent_list"
#   nc_quote "$reason"
#   nc_action "Проверь шину: dept-ledger list --to dept-tp"
#   NC_TG="$NOTIFY" nc_send   # или: NC_TG="$NOTIFY"; nc_send --chat-id 123

# Лимит Telegram — 4096 UTF-16 code units. Держим потолок ниже с запасом (тот же 3500, что
# уже выбран в bin/claude-auto-tg:101 для его собственного капа).
NC_BUDGET="${NC_BUDGET:-3500}"
# Файл, из которого telegram_notify.sh ЖЁСТКО грузит окружение (его строки 29-47 — литерал,
# не переменная). Валидировать токен в каком-то ДРУГОМ файле бессмысленно: нотификатор всё
# равно возьмёт значение отсюда. Поэтому дефолт — тот же литерал, а не
# ${CLAUDE_AUTO_OPERATOR_ENV}: рассогласование между «где проверили» и «откуда возьмут» было
# бы тихим и необнаружимым.
#
# ПОЧЕМУ ПЕРЕОПРЕДЕЛЕНИЕ ЗДЕСЬ БЕЗОПАСНО (в отличие от пути нотификатора, где оно закрыто
# швом T8). Результат этой проверки — только ВЫБОР БОТА: имя `RNR_WORKERS_BOT_TOKEN` из
# allowlist telegram_notify.sh либо пусто. Сам токен читает нотификатор из СВОЕГО
# захардкоженного .env, сюда он не попадает и через эту переменную не подменяется. Максимум,
# чего добивается подмена, — сообщение уйдёт другим из двух разрешённых ботов тому же
# получателю. Ни выполнения кода, ни перенаправления адресата (в отличие от $TELEGRAM_NOTIFY,
# где на кону запуск произвольного бинарника — потому там и стоит шов).
NC_ENV_FILE="${NC_ENV_FILE:-/home/rainor/server/.env}"
NC_DIVIDER="━━━━━━━━━━━━━━"
# Ширина, по которой переносятся списки элементов внутри секции. Не жёсткое требование
# Telegram, а читаемость: длинный список воркеров одной строкой снова превращается в «простыню».
NC_WRAP="${NC_WRAP:-58}"

# Вывод последней отправки (тело ответа нотификатора). Вызывающие, которым нужен message_id
# (claude-auto-tg/ask), его и так разбирают сами — здесь на случай диагностики.
NC_LAST_OUT=""

# --- примитивы -------------------------------------------------------------------------

# nc_esc <текст> — HTML-экранирование для parse_mode=HTML. Два неочевидных момента:
#
#  1. Порядок: & первым, иначе он повторно экранирует уже вставленные &lt;/&gt;.
#  2. `\&` в строке ЗАМЕНЫ обязателен. Начиная с bash 5.2 включён по умолчанию shopt
#     patsub_replacement, и голый `&` в замене ${s//pat/repl} раскрывается в СОВПАВШИЙ
#     текст (как в sed). Наивное `${s//</&lt;}` на bash 5.2 даёт «<lt;», а не «&lt;» —
#     то есть ломает ровно то, ради чего написано, и Telegram отвергает сообщение целиком.
#     Ровно этот баг был найден в html_escape у bin/claude-auto-self-probes (починен переводом
#     на эту функцию). В bin/claude-auto-ask экранирование делается через sed, где `\&` в
#     замене уже экранирован правильно, — там бага нет.
nc_esc() {
  local s="${1:-}"
  s="${s//&/\&amp;}"; s="${s//</\&lt;}"; s="${s//>/\&gt;}"
  printf '%s' "$s"
}

# nc_unesc <текст> — обратная операция для plain-фолбэка. Порядок ЗЕРКАЛЬНЫЙ: & последним,
# иначе "&amp;lt;" схлопнется в "<" вместо "&lt;". Здесь `&` стоит в ПАТТЕРНЕ, а не в замене,
# поэтому patsub_replacement (см. nc_esc) роли не играет.
nc_unesc() {
  local s="${1:-}"
  s="${s//&lt;/<}"; s="${s//&gt;/>}"; s="${s//&amp;/&}"
  printf '%s' "$s"
}

# nc_u16len <текст> — длина в UTF-16 code units, тех же единицах, которыми меряет Telegram.
# НЕ ${#s} и НЕ `cut -c`: первое считает кодовые точки, второе в этом окружении режет по
# БАЙТАМ независимо от локали. Для не-BMP символов (эмодзи — а они тут в каждом заголовке)
# кодовые точки и UTF-16 расходятся вдвое. Идиома взята из bin/dept-planerka-exec:87-92,
# где эта же ловушка уже разобрана. Без jq — консервативный фолбэк по кодовым точкам ×2.
nc_u16len() {
  local s="${1:-}"
  if command -v jq >/dev/null 2>&1; then
    printf '%s' "$s" | jq -Rs '(explode | map(select(. > 65535)) | length) + length' 2>/dev/null && return 0
  fi
  printf '%s' $(( ${#s} * 2 ))
}

# nc_trunc_u16 <текст> <лимит> — обрезка СЫРОГО текста до лимита в UTF-16 code units.
# Применяется только к значениям ДО вставки тегов: резать собранный HTML нельзя — разрез
# внутри тега или сущности даёт ровно тот 400, от которого защищаемся.
#
# Меряет и режет в ОДНИХ единицах. Прежняя редакция резала по кодовым точкам (`.[0:n]` в jq),
# и на не-BMP входе это врало вдвое: 3600 эмодзи «обрезанных до 3500» давали 6991 UTF-16 unit
# — вдвое выше лимита Telegram (4096), то есть сообщение всё равно отвергалось целиком.
# Хвостовое «…» само стоит 1 unit, поэтому набираем до lim-1.
nc_trunc_u16() {
  local s="${1:-}" lim="${2:-0}"
  [ "$(nc_u16len "$s")" -le "$lim" ] 2>/dev/null && { printf '%s' "$s"; return 0; }
  if command -v jq >/dev/null 2>&1; then
    printf '%s' "$s" | jq -Rsr --argjson n "$lim" '
      (explode
       | reduce .[] as $c ({a: [], n: 0};
           (if $c > 65535 then 2 else 1 end) as $w
           | if .n + $w <= ($n - 1) then {a: (.a + [$c]), n: (.n + $w)} else . end)
       | .a | implode) + "…"' 2>/dev/null && return 0
  fi
  # Без jq: ${s:0:N} режет по кодовым точкам, поэтому берём вдвое меньше — консервативно,
  # зато гарантированно не выше лимита даже если весь текст не-BMP.
  printf '%s…' "${s:0:$(( lim / 2 - 1 ))}"
}

# nc_botflag — имя env-переменной с токеном @RnR_Workers, если он в .env валиден; иначе пусто
# (вызывающий уйдёт дефолтным ботом). ЕДИНСТВЕННАЯ реализация этой проверки: до этого файла
# тот же regex был скопирован в claude-auto-{notify,tg,run}, event-bridge-watch и
# self-probes. Токен сломан или файл нечитаем → ПУСТО, а не отказ: доставка важнее
# косметики (вернётся шапка «🖥️ Сервер», но алерт доедет).
nc_botflag() {
  local val
  val="$(grep -E '^RNR_WORKERS_BOT_TOKEN=' "$NC_ENV_FILE" 2>/dev/null | head -1 \
          | sed -e 's/^RNR_WORKERS_BOT_TOKEN=//' -e 's/^["'\'' ]*//' -e 's/["'\'' ]*$//')"
  [[ "$val" =~ ^[0-9]+:[A-Za-z0-9_-]+$ ]] && printf 'RNR_WORKERS_BOT_TOKEN'
}

# nc_is_markup_error <вывод-нотификатора> — отличить ошибку РАЗМЕТКИ от транспортной.
# Ретраить вслепую нельзя: telegram_notify.sh при сетевом флапе тратит ~42с (direct 10 +
# vpn 15 + ssl-retry 15, его строки 418-422), и второй такой прогон вылезет за таймаут
# вызывающего. Ошибка разметки же возвращается API мгновенно — её ретрай дёшев.
nc_is_markup_error() {
  case "${1:-}" in
    *"can't parse entities"*|*"can t parse entities"*|*'"error_code":400'*) return 0 ;;
  esac
  return 1
}

# nc_is_auth_error <вывод> — отказ АВТОРИЗАЦИИ бота (отозванный токен, бот заблокирован).
# Отдельно от транспортного сбоя: только здесь осмысленно перепробовать ДРУГИМ ботом. При
# сетевом сбое второй бот пойдёт через ту же сеть и тот же telegram_notify.sh (~42с) —
# фолбэк лишь удвоил бы задержку, ничего не починив.
nc_is_auth_error() {
  case "${1:-}" in
    *'"error_code":401'*|*'"error_code":403'*|*Unauthorized*|*Forbidden*|*"bot was blocked"*) return 0 ;;
  esac
  return 1
}

# nc_plain <html> — деградация карточки в читаемый plain-текст для ретрая. Снять ТЕГИ и
# раскодировать сущности: наивный стриппер оставил бы "&lt;" видимым в тексте.
nc_plain() {
  local s="${1:-}"
  s="${s//<b>/}"; s="${s//<\/b>/}"
  s="${s//<i>/}"; s="${s//<\/i>/}"
  s="${s//<code>/}"; s="${s//<\/code>/}"
  nc_unesc "$s"
}

# --- сборка карточки -------------------------------------------------------------------

_NC_HEAD=""
_NC_FIELDS=""
_NC_QUOTE=""
_NC_ACTION=""
_NC_NOTE=""
_NC_SEC_EMOJI=()
_NC_SEC_TITLE=()
_NC_SEC_BODY=()
_NC_SEC_KIND=()

nc_reset() {
  _NC_HEAD=""; _NC_FIELDS=""; _NC_QUOTE=""; _NC_ACTION=""; _NC_NOTE=""
  _NC_SEC_EMOJI=(); _NC_SEC_TITLE=(); _NC_SEC_BODY=(); _NC_SEC_KIND=()
  NC_LAST_OUT=""
}

# nc_head <эмодзи> <заголовок> [подзаголовок]
nc_head() {
  local emoji="${1:-}" title="${2:-}" sub="${3:-}"
  _NC_HEAD="$(nc_esc "$emoji") <b>$(nc_esc "$title")</b>"
  [ -n "$sub" ] && _NC_HEAD="${_NC_HEAD} · $(nc_esc "$sub")"
  _NC_HEAD="${_NC_HEAD}
${NC_DIVIDER}"
}

# nc_field <ключ> <значение> — строка блока фактов. Пустое значение пропускается молча:
# «Итог: —» ничего не сообщает, а место занимает.
#
# Формат «Ключ — <b>значение</b>», а НЕ колонки, выровненные пробелами: Telegram рендерит
# обычный текст пропорциональным шрифтом, поэтому padding пробелами выравнивает только
# случайно (пока ключи одинаковой ширины) и разъезжается на первом же длинном ключе.
# Жирное значение даёт сканируемость без опоры на выравнивание.
nc_field() {
  local k="${1:-}" v="${2:-}"
  [ -n "$k" ] && [ -n "$v" ] || return 0
  local line
  line="$(nc_esc "$k") — <b>$(nc_esc "$v")</b>"
  if [ -n "$_NC_FIELDS" ]; then _NC_FIELDS="${_NC_FIELDS}
${line}"; else _NC_FIELDS="$line"; fi
}

# nc_section <эмодзи> <заголовок> <элементы> — секция-СПИСОК (элементы разделены пробелами).
# Пустой список → секции не будет вовсе (главное отличие от нынешнего «— нет»).
nc_section() {
  local emoji="${1:-}" title="${2:-}" items="${3:-}"
  [ -n "${items//[[:space:]]/}" ] || return 0
  _NC_SEC_EMOJI+=("$emoji"); _NC_SEC_TITLE+=("$title")
  _NC_SEC_BODY+=("$items"); _NC_SEC_KIND+=("list")
}

# nc_block <эмодзи> <заголовок> <текст> — секция со СВОБОДНЫМ текстом (хвост stderr, низ
# экрана воркера). Рендерится моноширинным <code>, чтобы вывод программы визуально
# отделялся от прозы карточки.
nc_block() {
  local emoji="${1:-}" title="${2:-}" text="${3:-}"
  [ -n "${text//[[:space:]]/}" ] || return 0
  _NC_SEC_EMOJI+=("$emoji"); _NC_SEC_TITLE+=("$title")
  _NC_SEC_BODY+=("$text"); _NC_SEC_KIND+=("block")
}

# nc_quote <текст> — причина/цитата человека.
nc_quote() { local t="${1:-}"; [ -n "${t//[[:space:]]/}" ] && _NC_QUOTE="$t"; return 0; }

# nc_action <текст> — что оператору сделать. Ставить ТОЛЬКО когда от него реально требуется
# действие: «Что делать» на каждой карточке обесценивает блок.
nc_action() { local t="${1:-}"; [ -n "${t//[[:space:]]/}" ] && _NC_ACTION="$t"; return 0; }

# nc_note <текст> — хвостовая ремарка обычным текстом (без эмодзи и заголовка). Для
# служебных оговорок вида «сессии не пересобирались»: это не факт карточки (в шапку не
# просится) и не действие оператора, а сноска.
nc_note() { local t="${1:-}"; [ -n "${t//[[:space:]]/}" ] && _NC_NOTE="$t"; return 0; }

# _nc_wrap <элементы> <макс-элементов> — список токенов в « · »-строки с отступом и
# переносом по NC_WRAP. cap=0 — без ограничения; иначе хвост сворачивается в «… (ещё N)».
_nc_wrap() {
  local items="${1:-}" cap="${2:-0}"
  local -a toks=(); local t
  # `set -f` на время разбиения: без него `for t in $items` делает не только word-splitting
  # (он нужен), но и PATHNAME EXPANSION — токен с `*`/`?`/`[` подставил бы в карточку имена
  # файлов из текущего каталога. Имена воркеров сегодня валидированы, но рендер не должен
  # зависеть от санитарности вызывающего.
  local _nc_noglob=0; case "$-" in *f*) _nc_noglob=1 ;; esac
  set -f
  # shellcheck disable=SC2086  # намеренное word-splitting: items — список токенов через пробел
  for t in $items; do toks+=("$t"); done
  [ "$_nc_noglob" = 1 ] || set +f
  # ДВА отдельных local, не `local total=… shown="$total"`: local — builtin, и bash
  # раскрывает ВСЕ его аргументы ДО присваивания, поэтому "$total" во втором слове взял бы
  # ещё не установленное (внешнее) значение и уехал пустым.
  local total="${#toks[@]}"
  local shown="$total"
  [ "$cap" -gt 0 ] && [ "$total" -gt "$cap" ] && shown="$cap"
  local out="" line="" i=0
  local e
  while [ "$i" -lt "$shown" ]; do
    e="$(nc_esc "${toks[$i]}")"
    if [ -z "$line" ]; then line="$e"
    # Ширина считается в КОДОВЫХ ТОЧКАХ (${#line}) — это контракт переноса, к которому
    # приведено и JS-зеркало (там пришлось явно брать [...line].length вместо .length,
    # иначе на эмодзи-списках рендеры расходились и паритет ломался).
    elif [ "${#line}" -ge "$NC_WRAP" ]; then out="${out}   ${line}
"; line="$e"
    else line="${line} · ${e}"; fi
    i=$((i + 1))
  done
  [ -n "$line" ] && out="${out}   ${line}"
  [ "$shown" -lt "$total" ] && out="${out} · … (ещё $((total - shown)))"
  printf '%s' "$out"
}

# _nc_render_parts <cap-элементов> <включать-цитату 0|1> <включать-факты 0|1> <кап-скаляров>
# Кап скаляров (0 = без капа) режет ДЛИННЫЕ ОДИНОЧНЫЕ значения — текст nc_block (хвост
# stderr, низ экрана) и цитату. Без него лестница вытеснения умела ужимать только СПИСКИ, а
# карточка с одним огромным блоком проваливалась в аварийную обрезку готового HTML.
_nc_render_parts() {
  local cap="${1:-0}" with_quote="${2:-1}" with_fields="${3:-1}" scap="${4:-0}"
  local -a parts=()
  # Блок фактов клеится к шапке ОДНИМ переводом строки, а не через пустую: разделитель ━━━
  # уже отбивает шапку, и вторая пустая строка подряд разрывает то, что читается как единое
  # «кто и что» карточки.
  local head="$_NC_HEAD"
  if [ "$with_fields" = 1 ] && [ -n "$_NC_FIELDS" ]; then
    if [ -n "$head" ]; then head="${head}
${_NC_FIELDS}"; else head="$_NC_FIELDS"; fi
  fi
  [ -n "$head" ] && parts+=("$head")
  local i=0
  while [ "$i" -lt "${#_NC_SEC_EMOJI[@]}" ]; do
    local body
    if [ "${_NC_SEC_KIND[$i]}" = "block" ]; then
      local btext="${_NC_SEC_BODY[$i]}"
      [ "$scap" -gt 0 ] && btext="$(nc_trunc_u16 "$btext" "$scap")"
      body="   <code>$(nc_esc "$btext")</code>"
    else
      body="$(_nc_wrap "${_NC_SEC_BODY[$i]}" "$cap")"
    fi
    parts+=("$(nc_esc "${_NC_SEC_EMOJI[$i]}") <b>$(nc_esc "${_NC_SEC_TITLE[$i]}")</b>
${body}")
    i=$((i + 1))
  done
  if [ "$with_quote" = 1 ] && [ -n "$_NC_QUOTE" ]; then
    local qtext="$_NC_QUOTE"
    [ "$scap" -gt 0 ] && qtext="$(nc_trunc_u16 "$qtext" "$scap")"
    parts+=("💬 <i>$(nc_esc "$qtext")</i>")
  fi
  [ -n "$_NC_NOTE" ] && parts+=("$(nc_esc "$_NC_NOTE")")
  [ -n "$_NC_ACTION" ] && parts+=("➡️ <b>Что делать</b>
   $(nc_esc "$_NC_ACTION")")
  local out="" p
  for p in "${parts[@]}"; do
    if [ -n "$out" ]; then out="${out}

${p}"; else out="$p"; fi
  done
  printf '%s' "$out"
}

# nc_render — собранная карточка. Бюджет применяется ПРИ СБОРКЕ (перерисовкой с меньшим
# содержимым), а не обрезкой готового HTML: разрез внутри тега/сущности сам по себе даёт
# 400. Порядок вытеснения — от наименее ценного к наиболее: длинные списки → цитата →
# блок фактов. Заголовок и «Что делать» выживают всегда.
nc_render() {
  local out fits
  fits() { [ "$(nc_u16len "$1")" -le "$NC_BUDGET" ] 2>/dev/null; }
  out="$(_nc_render_parts 0 1 1 0)"; fits "$out" && { printf '%s' "$out"; return 0; }
  # 1) ужимаем длинные ОДИНОЧНЫЕ значения (хвост stderr в nc_block, цитату)
  local scap
  for scap in 1200 400 120; do
    out="$(_nc_render_parts 0 1 1 "$scap")"; fits "$out" && { printf '%s' "$out"; return 0; }
  done
  # 2) ужимаем СПИСКИ
  local cap
  for cap in 24 12 6 3; do
    out="$(_nc_render_parts "$cap" 1 1 120)"; fits "$out" && { printf '%s' "$out"; return 0; }
  done
  # 3) выбрасываем цитату, затем блок фактов
  out="$(_nc_render_parts 3 0 1 120)"; fits "$out" && { printf '%s' "$out"; return 0; }
  out="$(_nc_render_parts 3 0 0 120)"; fits "$out" && { printf '%s' "$out"; return 0; }
  # 4) Патология (не влезает даже заголовок + «Что делать»). Готовый HTML НЕ режем — разрез
  # внутри тега или сущности сам по себе даёт 400. Разворачиваем в plain, режем СЫРОЙ текст
  # по UTF-16, экранируем обратно; экранирование раздувает строку (& → &amp;), поэтому
  # ужимаем в цикле, пока не влезет.
  local plain; plain="$(nc_plain "$out")"
  local lim="$NC_BUDGET"
  while [ "$lim" -gt 32 ]; do
    out="$(nc_esc "$(nc_trunc_u16 "$plain" "$lim")")"
    fits "$out" && { printf '%s' "$out"; return 0; }
    lim=$(( lim / 2 ))
  done
  printf '%s' "$out"
}

